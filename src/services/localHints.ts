export function getLocalHint(module: ModuleType, trigger: HintTrigger): string {
  const moduleHints = hints[module];
  if (!moduleHints) return 'ממשיכים! 💪';
  const triggerHints = moduleHints[trigger];
  if (!triggerHints || triggerHints.length === 0) return 'ממשיכים! 💪';
  return triggerHints[Math.floor(Math.random() * triggerHints.length)];
}

export function getAllHintsForModule(module: ModuleType): Record<HintTrigger, string[]> {
  return hints[module] || hints.vocabulary;
}
